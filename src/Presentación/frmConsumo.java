    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
   // TODO add your handling code here:
        /*COMENTARIO 
                PARA SUMAR LA CANTIDAD AL STOCK AL MOMENTO DE ELIMINAR UN PRODUCTO.*/
        String stk= txtprueba.getText();
        String cant= txtcantidad.getText();
        String idp= txtidproducto.getText();
        int aumentar = Integer.parseInt(stk)+Integer.parseInt(cant);
        txtprueba2.setText(Integer.toString(aumentar));
        
        vproducto vp= new vproducto();
        vp.setStock(Integer.parseInt(txtprueba2.getText()));
        vp.setIdproducto(Integer.parseInt(idp));
        fproducto fp= new fproducto();
        
        
        if (!txtidconsumo.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Est谩s seguro de Eliminar el Producto?","Confirmar",2);
            vp.setIdproducto(Integer.parseInt(idp));
            if(fp.ModificarStock(vp));
            mostrar2("");
            if (confirmacion==0) {
                fconsumo func = new fconsumo ();
                vconsumo dts= new vconsumo();

                dts.setIdconsumo(Integer.parseInt(txtidconsumo.getText()));
                func.eliminar(dts);
                mostrar(idreserva);
                inhabilitar();

            }

        }
                                          
    }//GEN-LAST:event_btneliminarActionPerformed

